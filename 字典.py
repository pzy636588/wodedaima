#通过键不是通过索引来读取
#字典是任意对象的无序集合
#字典是可变的，并且可以任意嵌套
#字典中的键必须唯一
#字典中的键必须不可变（字典中使用的键，可以使用数字，字符串或者元组，但不能使用列表）
#创建字典时，在“键”和“值”之间使用冒号分隔，相邻两个元素使用逗号分隔，所有元素放在"{}"中
#例子：
#dictionary={'key1':'value1','key2':'value2',....,'keyn':'valuen',}
#dictionary:字典名称
#key:表示元素的值，必须是唯一的，并且不可变
#value1,value2,...valuen:表示元素的值，可以是任何数据类型，不是必须唯一的
#同列表一和元组一样，也可以创建空字典，有以下两种方法
#dictionary={}或者dictionary=dict()